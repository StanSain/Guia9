package entidades;

/**
 *
 * @author John
 */
public class caja {
    private String titular;
    private int saldo;

    public caja() {
    }

    public caja(String titular, int saldo) {
        this.titular = titular;
        this.saldo = saldo;
    }

    public String getTitular() {
        return titular;
    }

    public void setTitular(String titular) {
        this.titular = titular;
    }

    public int getSaldo() {
        return saldo;
    }

    public void setSaldo(int saldo) {
        this.saldo = saldo;
    }

    @Override
    public String toString() {
        return "caja{" + "titular=" + titular + ", saldo=" + saldo + '}';
    }
    
}
